const fontSelection = document.querySelector("#font-selection");
const colorSelection = document.querySelector("#color-selection");
const preview = document.querySelector("#preview-placeholder");

fonts.forEach((font) => {
  const option = document.createElement("option");
  option.value = font;
  option.textContent = font;
  fontSelection.append(option);
});

colors.forEach((color) => {
  const option = document.createElement("option");
  option.value = color;
  option.textContent = color;
  colorSelection.append(option);
});

document.querySelector("#text-input").addEventListener("input", (event) => {
  preview.textContent = event.target.value;
});

colorSelection.addEventListener("change", () => {
  preview.style.color = colorSelection.value;
});

fontSelection.addEventListener("change", () => {
  preview.style.fontFamily = fontSelection.value;
});

document.querySelector("#save-button").addEventListener("click", () => {
  const color = colorSelection.value;
  const font = fontSelection.value;
  const savedTextItem = document.createElement("li");
  savedTextItem.textContent = preview.textContent;
  savedTextItem.style.color = color;
  savedTextItem.style.fontFamily = font;
  document.querySelector("ul").append(savedTextItem);
});
